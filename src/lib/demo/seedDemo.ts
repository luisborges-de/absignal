type DriftableMetric =
  | 'occupancy'
  | 'dscr'
  | 'ltv'
  | 'topTenant'
  | 'wart'
  | 'reserve'
  | 'pue'
  | 'powerCost'

export interface SnapshotProfile {
  idPrefix: string
  totalCapacityMW: number
  scheduledDebtService: number
  seniorDebtService: number
  outstandingStart: number
  monthlyAmortisation: number
  contractedRevenueStart: number
  contractedRevenueDrift: number
  grossRevenueStart: number
  grossRevenueDrift: number
  operatingExpensesStart: number
  operatingExpensesDrift: number
  tenantCountStart: number
  tenantCountDrift: number
  seniorInterestReserveBalance: number
  requiredReserveBalance: number
  start: Record<DriftableMetric, number>
  drift: Record<DriftableMetric, number>
  series?: Partial<Record<DriftableMetric, readonly number[]>>
  notes: string
}

function metricAt(profile: SnapshotProfile, metric: DriftableMetric, index: number) {
  const fromSeries = profile.series?.[metric]?.[index]
  if (typeof fromSeries === 'number') return fromSeries
  return profile.start[metric] + profile.drift[metric] * index
}

function round(value: number, digits: number) {
  const factor = 10 ** digits
  return Math.round(value * factor) / factor
}

export function generateSnapshots(dealId: string, profile: SnapshotProfile): PerformanceSnapshot[] {
  return PERIOD_DATES.map((periodDate, index) => {
    const occupancyRate = round(metricAt(profile, 'occupancy', index), 4)
    const dscr = round(metricAt(profile, 'dscr', index), 4)
    const ltv = round(metricAt(profile, 'ltv', index), 4)
    const topTenantRevenuePct = round(metricAt(profile, 'topTenant', index), 4)
    const weightedAvgRemainingLeaseTerm = round(metricAt(profile, 'wart', index), 2)
    const expenseReserveBalance = Math.round(metricAt(profile, 'reserve', index))
    const pueRatio = round(metricAt(profile, 'pue', index), 3)
    const powerCostPerKwh = round(metricAt(profile, 'powerCost', index), 4)

    const netCashFlow = Math.round(profile.scheduledDebtService * dscr)
    const outstandingBalance = profile.outstandingStart - profile.monthlyAmortisation * index

    return {
      id: `${profile.idPrefix}-0000-4000-8000-${String(index + 1).padStart(12, '0')}`,
      dealId,
      periodDate,
      occupancyRate,
      leasedCapacityMW: round(profile.totalCapacityMW * occupancyRate, 2),
      totalCapacityMW: profile.totalCapacityMW,
      contractedRevenue: Math.round(profile.contractedRevenueStart + profile.contractedRevenueDrift * index),
      grossRevenue: Math.round(profile.grossRevenueStart + profile.grossRevenueDrift * index),
      operatingExpenses: Math.round(profile.operatingExpensesStart + profile.operatingExpensesDrift * index),
      netCashFlow,
      scheduledDebtService: profile.scheduledDebtService,
      seniorDebtService: profile.seniorDebtService,
      dscr,
      seniorDscr: round(netCashFlow / profile.seniorDebtService, 2),
      pueRatio,
      powerCostPerKwh,
      topTenantRevenuePct,
      tenantCount: Math.round(profile.tenantCountStart + profile.tenantCountDrift * index),
      weightedAvgRemainingLeaseTerm,
      outstandingBalance,
      appraisedValue: Math.round(outstandingBalance / ltv),
      ltv,
      seniorInterestReserveBalance: profile.seniorInterestReserveBalance,
      expenseReserveBalance,
      requiredReserveBalance: profile.requiredReserveBalance,
      source: 'DEMO',
      notes: profile.notes,
      createdAt: `${periodDate}T00:00:00.000Z`,
    }
  })
}

