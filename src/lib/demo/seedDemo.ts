function approvedRulesForDeal(dealId: string) {
  return demoTriggerRules.filter(
    (rule) => rule.dealId === dealId && rule.extractionStatus === 'APPROVED',
  )
}

export function demoEvaluationsForSnapshot(snapshotId: string): TriggerEvaluation[] {
  const snapshot = demoSnapshots.find((item) => item.id === snapshotId) ?? demoSnapshots.at(-1)
  if (!snapshot) return []
  const scopedSnapshots = demoSnapshots.filter(
    (item) => item.dealId === snapshot.dealId && item.periodDate <= snapshot.periodDate,
  )
  return evaluateAllTriggers(approvedRulesForDeal(snapshot.dealId), scopedSnapshots)
}

export const demoEvaluations = demoSnapshots.flatMap((snapshot) =>
  demoEvaluationsForSnapshot(snapshot.id),
)

