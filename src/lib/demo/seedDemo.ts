interface RuleSpec {
  id: string
  family: TriggerFamily
  name: string
  description: string
  metricKey: string
  operator: TriggerOperator
  threshold: number | null
  thresholdUnit: string
  lookbackPeriods?: number
  consequence: TriggerConsequence
  sectionReference: string
  sourceText: string
  extractionStatus?: ExtractionStatus
  extractionConfidence?: number
  watchBuffer?: number
}

function makeRule(dealId: string, spec: RuleSpec): TriggerRule {
  return {
    id: spec.id,
    dealId,
    family: spec.family,
    name: spec.name,
    description: spec.description,
    metricKey: spec.metricKey,
    operator: spec.operator,
    threshold: spec.threshold,
    thresholdUnit: spec.thresholdUnit,
    lookbackPeriods: spec.lookbackPeriods ?? 1,
    consequence: spec.consequence,
    sectionReference: spec.sectionReference,
    sourceText: spec.sourceText,
    extractionStatus: spec.extractionStatus ?? 'APPROVED',
    extractionConfidence: spec.extractionConfidence ?? 0.9,
    watchBuffer: spec.watchBuffer ?? 0.1,
    active: true,
    createdAt: now,
    updatedAt: now,
  }
}

